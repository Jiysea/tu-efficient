    protected function getIdNumber($type_of_id)
    {
        $id_number = '';

        switch ($type_of_id) {
            case 'Driver\'s License':
                $id_number = fake()->bothify('?##-##-######');
                break;
            case 'Philippine Identification (PhilID / ePhilID)':
                $id_number = fake()->bothify('####-###-####-#');
                break;
            case 'BIR (TIN)':
                $id_number = fake()->bothify('###-###-###-###');
                break;
            case 'Phil-health ID':
                $id_number = fake()->bothify('##-#########-#');
                break;
            case 'Philippine Postal ID':
                $id_number = fake()->bothify('############-?');
                break;
            case 'NBI Clearance':
                $id_number = fake()->bothify('????##?##?-?####?###');
                break;
            default:
                $id_number = fake()->bothify('######');
                break;
        }
        return strtoupper($id_number);
    }
